package operators;

public class QUE1 {
	
	public static void main(String[] args) {
		
		int a = 8;
		
		int b = 10;
		
		int c = a + b;

		int d = a - b;
		
		int e = a * b;
		
		int f = a / b;
		
		int g = a % b;
		
		System.out.println("Sum result "+c);
		
		System.out.println("Minus result "+d);
		
		System.out.println("Multiplication result "+e);
		
		System.out.println("Quotient "+f);
		
		System.out.println("Reminder "+g);
		
	}

}
